#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	index;
	char	*str;

	str = (char *)s;
	if (n == 0)
		return ;
	index = 0;
	while (n > 0)
	{
		str[index] = '\0';
		n--;
		index++;
	}
}
